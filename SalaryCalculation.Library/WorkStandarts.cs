namespace SalaryCalculation.Library
{
    class WorkStandarts
    {
        public const byte DAYS = 5;
        public const byte WEEKS = 4;
        public const byte HOURS_IN_DAY = 8;
        public const byte HOURS_IN_WEEK = HOURS_IN_DAY * DAYS;
        public const byte HOURS_IN_MONTH = HOURS_IN_WEEK * WEEKS;
    }
}
