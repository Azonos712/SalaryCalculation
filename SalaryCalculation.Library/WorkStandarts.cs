namespace SalaryCalculation.Library
{
    class WorkStandarts
    {
        public const byte WORK_DAYS = 5;
        public const byte WORK_WEEKS = 4;
        public const byte HOURS_IN_WORK_DAY = 8;
        public const byte HOURS_IN_WORK_WEEK = HOURS_IN_WORK_DAY * WORK_DAYS;
        public const byte HOURS_IN_WORK_MONTH = HOURS_IN_WORK_WEEK * WORK_WEEKS;
    }
}
